#Indexed array
print("<= Indexed array =>")
kota = ["Tangerang", "Jakarata", "Bandung", "Surabaya"]
print(kota)
print()

#Multidimensional array
print("<= Multidimensional array =>")
num = [[2, 4, 6, 8, 10], [3, 6, 9, 12, 15], [4, 8, 12, 16, 20]]
for record in num:
    print(record)
print()

#Associative array
print("<= Associative array =>")
a_dict = dict()

a_dict["name"] = "John"
a_dict["age"] = "20"
a_dict["major"] = "Computer Science"

print(a_dict)